#!/bin/bash

# Move forward 1 second
./mf1
sleep 0.5

# Move backward 1 second
./mb1
sleep 0.5

# Turn right 90 degrees
./tr90
sleep 0.5

# Turn left 90 degrees
./tl90
sleep 0.5

# Change direction by turning right(Rotate 180 degree right)
./tr180
sleep 0.5

# Change direction by turning left(Rotate 180 degree left)
./tl180
sleep 0.5
